// Introduction to Arrays in JavaScript

// Creating an Array
let fruits = ["apple", "banana", "cherry"];
console.log("Fruits Array:", fruits);

// Accessing Array Elements
// Accessing the first element (index 0)
console.log("First Fruit:", fruits[0]); // Output: apple

// Accessing the third element (index 2)
console.log("Third Fruit:", fruits[2]); // Output: cherry

// Modifying Array Elements
// Changing the second element
fruits[1] = "blueberry";
console.log("Modified Fruits Array:", fruits);

// Array Length
// Getting the number of elements in the array
console.log("Number of Fruits:", fruits.length);

// Adding Elements to an Array using push()
fruits.push("mango");
console.log("Added Mango:", fruits);

// Removing the last element from an Array using pop()
fruits.pop();
console.log("Removed Last Fruit:", fruits);

// Conclusion
// Arrays are powerful and flexible structures for storing ordered data.
// They have various methods to manipulate the data they hold.
